export const DAILY_CHALLENGE_SLOT_MS = 6 * 60 * 60 * 1000

const getLocalDateKey = (date) => {
  const year = date.getFullYear()
  const month = String(date.getMonth() + 1).padStart(2, '0')
  const day = String(date.getDate()).padStart(2, '0')
  return `${year}-${month}-${day}`
}

export const getDailyChallengeSlotInfo = (date = new Date()) => {
  const slotIndex = Math.floor(date.getHours() / 6)
  const dateKey = getLocalDateKey(date)
  const nextUnlockAt = new Date(date)
  nextUnlockAt.setHours((slotIndex + 1) * 6, 0, 0, 0)

  return {
    dateKey,
    slotIndex,
    slotId: `${dateKey}-${slotIndex}`,
    nextUnlockAt: nextUnlockAt.getTime(),
    msUntilNextSlot: Math.max(0, nextUnlockAt.getTime() - date.getTime()),
  }
}

export const getChallengeForTopic = (topicId, date = new Date()) => {
  const topicChallenges = dailyChallenges.filter(c => c.topicId === topicId)
  const safeChallenges = topicChallenges.length ? topicChallenges : dailyChallenges.filter(c => c.topicId === 'variables')
  const dayIndex = Math.floor(new Date(date.getFullYear(), date.getMonth(), date.getDate()).getTime() / (24 * 60 * 60 * 1000))
  const { slotIndex } = getDailyChallengeSlotInfo(date)
  const index = ((dayIndex * 4) + slotIndex) % safeChallenges.length
  return safeChallenges[index]
}
